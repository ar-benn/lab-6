#include <stdio.h>
#include <stdlib.h>

int search(int numbers[], int low, int high, int value)
{
    if (high >= low) {
        int midPoint = low + (high - low) / 2;
 
        //check if element already in middle
        if (numbers[midPoint] == value)
            return midPoint;
 
        //if element is less than middle must be on left
        if (numbers[midPoint] > value)
            return search(numbers, low, midPoint - 1, value);
 
        // otherwise, element must be on right
        return search(numbers, midPoint + 1, high, value);
    }
 
    //return if element cannot be found
    return -1;
}

void printArray(int numbers[], int sz)
{
	int i;
	printf("Number array : ");
	for (i = 0;i<sz;++i)
	{
		printf("%d ",numbers[i]);
	}
	printf("\n");
}

int main(void)
{
	int i, numInputs;
	char* str;
	float average;
	int value;
	int index;
	int* numArray = NULL;
	int countOfNums;
	FILE* inFile = fopen("input.txt","r");

	fscanf(inFile, " %d\n", &numInputs);
	
	while (numInputs-- > 0)
	{
		fscanf(inFile, " %d\n", &countOfNums);
		numArray = (int *) malloc(countOfNums * sizeof(int));
		average = 0;
		for (i = 0; i < countOfNums; i++)
		{
			fscanf(inFile," %d", &value);
			numArray[i] = value;
			average += numArray[i];
		}

		printArray(numArray, countOfNums);
		value = average / countOfNums;
		index = search(numArray, 0, countOfNums - 1, value);
		if (index >= 0)
		{
			printf("Item %d exists in the number array at index %d!\n",value, index);
		}
		else
		{
			printf("Item %d does not exist in the number array!\n", value);
		}

		free(numArray);
	}

	fclose(inFile);
}